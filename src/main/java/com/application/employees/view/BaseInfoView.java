package com.application.employees.view;

/**
 * The Class BaseInfoView.
 *
 * @author Aditya Rawal
 */
public class BaseInfoView
{
    
    /** The msg. */
    private String msg;

    /** The code. */
    private int code;

    /**
     * Gets the msg.
     *
     * @return the msg
     */
    public String getMsg()
    {
        return msg;
    }

    /**
     * Sets the msg.
     *
     * @param msg the new msg
     */
    public void setMsg(String msg)
    {
        this.msg = msg;
    }

    /**
     * Gets the code.
     *
     * @return the code
     */
    public int getCode()
    {
        return code;
    }

    /**
     * Sets the code.
     *
     * @param code the new code
     */
    public void setCode(int code)
    {
        this.code = code;
    }

}
